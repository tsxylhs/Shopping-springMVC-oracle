package com.lhs.shopping.entity;

public class cartList {

	private int cartListId;
	private int goodsCarId;
	private int goodsId;
	private int goodsCount;
	public int getCartListId() {
		return cartListId;
	}
	public void setCartListId(int cartListId) {
		this.cartListId = cartListId;
	}
	public int getGoodsCarId() {
		return goodsCarId;
	}
	public void setGoodsCarId(int goodsCarId) {
		this.goodsCarId = goodsCarId;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public int getGoodsCount() {
		return goodsCount;
	}
	public void setGoodsCount(int goodsCount) {
		this.goodsCount = goodsCount;
	}
	
}
